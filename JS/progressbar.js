//powering progressbar

function updateprogressbar1(progressBar1,value) {
    value= Math.round(value);
    progressBar1.querySelector(".progress__fill1").style.width= `${value}%`;
    progressBar1.querySelector(".progress__text1").textContent= `${value}%`;
}
  
const pbar1 = document.querySelector(".custom_progressbar1");
  
updateprogressbar1(pbar1, 97);
//endfunction updateprogressbar1(progressBar1,value)

